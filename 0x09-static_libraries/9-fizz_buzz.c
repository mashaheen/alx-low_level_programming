#include "main.h"

/**
 * main - check the code
 *
 * Return: Always 0.
 */
int main(void)
{
	int i;
	char fizz[] = "Fizz";
	char buzz[] = "Buzz";
	char fizzBuzz[] = "FizzBuzz";

	for (i = 1; i <= 100; i++)
	{
		if ( i % 3 == 0 && i % 5 == 0)
		{
			printf ("%s ", fizzBuzz);
		}else if (i % 3 == 0)
		{
			printf("%s ", fizz);
		}else if (i % 5 == 0)
		{
			printf("%s ", buzz);
		}
		else
		{
			printf ("%d ", i);
		}
	}
	putchar('\n');
       	return (0);
}
